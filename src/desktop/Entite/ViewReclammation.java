package desktop.Entite;

/**
 *
 * @author USER
 */
public class ViewReclammation {
    int id;
    String user;
    String sujet;
    String contenu;
    String date;
    String etat;

    public ViewReclammation(int id, String user, String sujet, String contenu, String date, String etat) {
        this.id = id;
        this.user = user;
        this.sujet = sujet;
        this.contenu = contenu;
        this.date = date;
        this.etat = etat;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getSujet() {
        return sujet;
    }

    public void setSujet(String sujet) {
        this.sujet = sujet;
    }

    public String getContenu() {
        return contenu;
    }

    public void setContenu(String contenu) {
        this.contenu = contenu;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getEtat() {
        return etat;
    }

    public void setEtat(String etat) {
        this.etat = etat;
    }
    
    
    
}
